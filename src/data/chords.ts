export function searchChords(query: string): Chord[] {
  const q = query.toLowerCase().trim();
  if (!q) return allChords;
  return allChords.filter(c =>
    c.label.toLowerCase().includes(q) ||
    c.key.toLowerCase().includes(q) ||
    `${c.key}${c.suffix}`.toLowerCase().includes(q) ||
    `${c.key} ${c.suffix}`.toLowerCase().includes(q)
  );
}

// ── Custom voicing persistence ─────────────────────────────
const CUSTOM_VOICINGS_KEY = "chord-library-custom-voicings";

export interface CustomChordData {
  chordKey: string;
  suffix: string;
  label: string;
  voicing: ChordVoicing;
}

export function getCustomVoicings(): CustomChordData[] {
  try {
    const data = localStorage.getItem(CUSTOM_VOICINGS_KEY);
    return data ? JSON.parse(data) : [];
  } catch { return []; }
}

export function saveCustomVoicing(data: CustomChordData) {
  const existing = getCustomVoicings();
  existing.push(data);
  localStorage.setItem(CUSTOM_VOICINGS_KEY, JSON.stringify(existing));
}

export function deleteCustomVoicing(index: number) {
  const existing = getCustomVoicings();
  existing.splice(index, 1);
  localStorage.setItem(CUSTOM_VOICINGS_KEY, JSON.stringify(existing));
}

export function getAllChordsWithCustom(): Chord[] {
  const customs = getCustomVoicings();
  const merged = chordData.map(c => ({ ...c, voicings: [...c.voicings] }));

  for (const custom of customs) {
    const existing = merged.find(c => c.key === custom.chordKey && c.suffix === custom.suffix);
    if (existing) {
      existing.voicings.push({ ...custom.voicing, isCustom: true });
    } else {
      merged.push({
        key: custom.chordKey,
        suffix: custom.suffix,
        label: custom.label,
        voicings: [{ ...custom.voicing, isCustom: true }],
      });
    }
  }
  return merged;
}
